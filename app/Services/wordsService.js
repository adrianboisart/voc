    var lastCurrentWord = null;
    var hasUndoBeenDone = false;

    Array.prototype.randomElement = function () {
        return this[Math.floor(Math.random() * this.length)];
    }

    var wordsToTest = null;   

    var initLastWordList= function(nbSkip, nbTake) {
        wordsToTest = wordObjects.slice(nbSkip, nbSkip + nbTake);
    }

    var getNextWord = function (currentWord) {
        if (hasUndoBeenDone) {
            hasUndoBeenDone = false;
            if (lastCurrentWord) return lastCurrentWord;
        }

        var nextWord = null;
        do {
            nextWord = wordsToTest.randomElement();
        } while (wordsToTest.length > 1 && currentWord === nextWord)
        lastCurrentWord = currentWord;
        return nextWord;
    }

    var excludeWord = function(word) {
        wordsToTest.splice(wordsToTest.indexOf(word), 1);
        lastExcludedWord = word;
    };

    var tryUndoExcludeWord= function() {
        if (lastExcludedWord) {
            wordsToTest.push(lastExcludedWord);
            lastExcludedWord = null;
        }
    }

    wordsServicefactory.undo = function () {
        tryUndoExcludeWord();
        hasUndoBeenDone = true;
    }

    var nbOfWordsToTest = function () {
        return wordsToTest.length;
    }

    wordsServicefactory.nbOfWordsToTest = nbOfWordsToTest;
    wordsServicefactory.excludeWord = excludeWord;
    wordsServicefactory.initLastWordList = initLastWordList;
    wordsServicefactory.getNextWord = getNextWord;

    return wordsServicefactory;
